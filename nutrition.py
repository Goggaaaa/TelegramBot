"""
Nutrition plans module for the Fitness Telegram bot.

Provides detailed monthly nutrition plans for weight loss and muscle gain (набор массы).
"""

weight_loss_plan = """
Питание для похудения (Месячный план)

Общие рекомендации:
- Дефицит калорий – потребляйте меньше калорий, чем тратите.
- Ешьте дробно: 5-6 приемов пищи в день.
- Включайте в рацион много овощей и белка.
- Избегайте сахаросодержащих и сильно обработанных продуктов.
- Пейте не менее 2 литров воды в день.

Примерный план по неделям:

Неделя 1-2:
- Завтрак: омлет из 2 яиц с овощами + цельнозерновой тост.
- Перекус: натуральный йогурт или творог с ягодами.
- Обед: куриная грудка, тушеные овощи, киноа или гречка.
- Полдник: горсть орехов и яблоко.
- Ужин: рыба или нежирное мясо, салат из свежих овощей, овощи на пару.

Неделя 3-4:
- Завтрак: овсяная каша на воде с фруктами и орехами.
- Перекус: зеленый смузи из шпината, яблока и киви.
- Обед: запеченная индейка, овощной салат с оливковым маслом.
- Полдник: морковные палочки с хумусом.
- Ужин: овощное рагу с тофу или курицей.

Советы:
- Ограничьте соль и сахар.
- Избегайте поздних перекусов.
- Ведите пищевой дневник для контроля.

"""

muscle_gain_plan = """
Питание для набора массы (Месячный план)

Общие рекомендации:
- Потребляйте избыток калорий для роста мышц.
- Высокий прием белка (1.6-2.2 г на кг веса).
- Сложные углеводы и полезные жиры для энергии.
- Прием пищи 5-6 раз в день для постоянного снабжения.

Примерный план по неделям:

Неделя 1-2:
- Завтрак: омлет из 4 яиц с овощами, овсянка с медом и орехами.
- Перекус: протеиновый коктейль и банан.
- Обед: говядина или курица, коричневый рис, овощи.
- Полдник: творог с ягодами.
- Ужин: лосось, картофель в кожуре, овощной салат.
- Перед сном: казеиновый белок или натуральный йогурт.

Неделя 3-4:
- Завтрак: смузи с протеином, овсянкой, арахисовой пастой и ягодами.
- Перекус: орехи микс и яблоко.
- Обед: индейка, киноа, салат с оливковым маслом.
- Полдник: яйца вкрутую и овощи.
- Ужин: тунец, паста из твердых сортов пшеницы, овощи.
- Перед сном: творог или белковый батончик.

Советы:
- Не пропускайте приемы пищи.
- Пейте много воды.
- Включайте в рацион разнообразные источники белка и углеводов.
- Следите за прогрессом и корректируйте калорийность.

"""

def get_plan_text(plan_type: str) -> str:
    """
    Возвращает текст питания по типу плана.
    plan_type: 'weight_loss' или 'muscle_gain'
    """
    if plan_type == 'weight_loss':
        return weight_loss_plan
    elif plan_type == 'muscle_gain':
        return muscle_gain_plan
    else:
        return "Выберите корректный план питания: 'weight_loss' или 'muscle_gain'."

